    private void loadCustomerDetailsById(String userId) {
        try{
           cust=CustomerDAO.getCustomerDetailsById(userId);
           this.txtAddress.setText(cust.getAddress());
           this.txtMobileNo.setText(cust.getMobileNo());
           this.txtName.setText(cust.getCustomerName());
           String password=PasswordEncryption.getDecryptedPassword(cust.getPassword());
           this.txtPassword.setText(password);
                   
           
           
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"DB error in CustomerAccountFrame");
            ex.printStackTrace();
           
        }
     
    }
      public boolean validateInputs(){
        char[]pwd=this.txtPassword.getPassword();
        if(pwd.length==0||this.txtName.getText().trim().isEmpty()||this.txtAddress.getText().trim().isEmpty()||this.txtMobileNo.getText().trim().isEmpty()||this.txtemailId.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Please fill all the details");
            return false;
        }
   
         if(Validator.isValidMobile(this.txtMobileNo.getText().trim())==false){
            JOptionPane.showMessageDialog(null,"Please input a valid mobile No");
            return false;
        }
        return true;
    }
          private void clearAll() {
        this.txtAddress.setText("");
        this.txtMobileNo.setText("");
        this.txtName.setText("");
        this.txtPassword.setText("");
        this.txtemailId.setText("");
        this.txtName.requestFocus();
    }
