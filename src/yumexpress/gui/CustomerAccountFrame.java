    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
      
     if(!validateInputs()){
           return;
       }
       try{
            cust.setCustomerName(txtName.getText().trim());
            cust.setMobileNo(txtMobileNo.getText().trim());
            cust.setAddress(txtAddress.getText().trim());
            char []pwd=txtPassword.getPassword();
            String pwdStr=String.valueOf(pwd);
            pwdStr=PasswordEncryption.getEncryptedPassword(pwdStr);
            cust.setPassword(pwdStr);
            boolean result=CustomerDAO.updateCustomer(cust);
            if(result){
                JOptionPane.showMessageDialog(null,"Customer Updation Successfull");
                clearAll();
                return;            
            }
            JOptionPane.showMessageDialog(null,"Updation UnSuccessfull");
            
        }catch(SQLException sq){
            JOptionPane.showMessageDialog(null,"DB ERROR IN CustomerRegistrationFrame");
            sq.printStackTrace();
        }
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

