    private void showFoodDetailsByIndex(int foodIndex) {     
        
        product=foodList.get(foodIndex);
        String foodName=product.getProductName();        
        if(foodName.length()>=20){
            foodName=foodName.substring(0,20)+"...";       
        }
        txtFoodName.setText(foodName);       
        Image img=product.getProductImage();
        img=img.getScaledInstance(lblFoodImage.getWidth(),lblFoodImage.getHeight(), Image.SCALE_SMOOTH);       
        ImageIcon icon=new ImageIcon(img);
        lblFoodImage.setIcon(icon);    
    }
    private boolean validateInputs(){
        if(this.txtFoodName.getText().isEmpty()||this.txtFoodPrice.getText().isEmpty()||this.lblFoodImage.getIcon()==null){
            return false;
        }
        return true;
    }

    private void clearAll() {
       this.txtFoodName.setText("");
       this.txtSearchFood.setText("");
       this.txtFoodPrice.setText("");
       this.lblFoodImage.setIcon(null);
       this.lblNext.setEnabled(false);
       this.lblPrev.setEnabled(false);
       this.txtSearchFood.requestFocus();
    }
    
