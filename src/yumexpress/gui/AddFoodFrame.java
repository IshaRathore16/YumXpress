    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String searchFood=this.txtSearchFood.getText().trim();
        if(searchFood.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please type a food");
            return;
        }
        try{
            searchFood=searchFood.replaceAll(" ", "-");
            foodList=SpoonacularAPI.getAllItemDetailsByName(searchFood);
            if(foodList.isEmpty()){
                JOptionPane.showMessageDialog(null,"No food details for "+searchFood+" found");
                return;
            }
            foodIndex=0;
            this.lblNext.setEnabled(true);
            this.lblPrev.setEnabled(true);
            showFoodDetailsByIndex(foodIndex);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Exception while using API");
            ex.printStackTrace();
            
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void lblNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblNextMouseClicked
        // TODO add your handling code here:
        foodIndex++;
        if(foodIndex>=foodList.size())
            foodIndex=0;
        showFoodDetailsByIndex(foodIndex);
        
    }//GEN-LAST:event_lblNextMouseClicked

    private void lblPrevMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblPrevMouseClicked
        // TODO add your handling code here:
        foodIndex--;
        if(foodIndex<0)
            foodIndex=foodList.size()-1;
        showFoodDetailsByIndex(foodIndex);
    }//GEN-LAST:event_lblPrevMouseClicked

    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        // TODO add your handling code here:
        if(!validateInputs()){
            JOptionPane.showMessageDialog(null,"Please fill all the data");
            return;
        }
        try{
            double price=Double.parseDouble(this.txtFoodPrice.getText().trim());
            product.setCompanyId(OwnerProfile.getCompanyId());
            product.setProductName(this.txtFoodName.getText().trim());
            product.setProductPrice(price);
            boolean res=ProductDAO.addProduct(product);
            if(!res){
                JOptionPane.showMessageDialog(null,"Cannot add the product");
                return;
            }
            JOptionPane.showMessageDialog(null,"Product Successfully added");
            foodList.remove(foodIndex);
            if(foodList.isEmpty()){
                JOptionPane.showMessageDialog(null,"All varieties of "+this.txtSearchFood.getText()+" added");
                clearAll();
                return;
            }
            foodIndex=0;
            showFoodDetailsByIndex(foodIndex);
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error");
            ex.printStackTrace();
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please input numeric values for the price");
            ex.printStackTrace();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null,"Exception in DAO Image Processing");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddItemActionPerformed

